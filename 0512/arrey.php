<?php
// サンリオキャラクターの二次元配列
// 各キャラクターの情報：名前、誕生年、種類、メインカラー

// 二次元配列の作成
$sanrio_characters = [
    // [名前, 誕生年, 種類, メインカラー]
    ["ハローキティ", 1974, "猫", "白とピンク"],
    ["マイメロディ", 1975, "うさぎ", "ピンク"],
    ["シナモロール", 2001, "子犬", "白と水色"],
    ["ポムポムプリン", 1996, "犬", "黄色"],
    ["クロミ", 2005, "うさぎ", "黒とピンク"],
    ["けろけろけろっぴ", 1988, "カエル", "緑"],
    ["バッドばつ丸", 1993, "ペンギン", "黒と赤"],
    ["ポチャッコ", 1989, "犬", "白と茶色"],
    ["タキシードサム", 1979, "ペンギン", "黒と白"],
    ["リトルツインスターズ", 1975, "星の子", "水色とピンク"]
];

// 配列の内容を表示
echo "<h1>サンリオキャラクター一覧</h1>";
echo "<table border='1'>";
echo "<tr><th>名前</th><th>誕生年</th><th>種類</th><th>メインカラー</th></tr>";

// 配列をループして表示
foreach ($sanrio_characters as $character) {
    echo "<tr>";
    echo "<td>" . $character[0] . "</td>"; // 名前
    echo "<td>" . $character[1] . "</td>"; // 誕生年
    echo "<td>" . $character[2] . "</td>"; // 種類
    echo "<td>" . $character[3] . "</td>"; // メインカラー
    echo "</tr>";
}

echo "</table>";

// 特定のキャラクターの情報を取得する例
echo "<h2>特定のキャラクター情報</h2>";
echo "3番目のキャラクター: " . $sanrio_characters[2][0] . "（" . $sanrio_characters[2][2] . "）";

// 二次元配列の別の表現方法（連想配列）
echo "<h2>連想配列を使用した二次元配列</h2>";

$sanrio_characters_assoc = [
    [
        "名前" => "ハローキティ",
        "誕生年" => 1974,
        "種類" => "猫",
        "メインカラー" => "白とピンク"
    ],
    [
        "名前" => "マイメロディ",
        "誕生年" => 1975,
        "種類" => "うさぎ",
        "メインカラー" => "ピンク"
    ],
    [
        "名前" => "シナモロール",
        "誕生年" => 2001,
        "種類" => "子犬",
        "メインカラー" => "白と水色"
    ]
];

// 配列が来たらforeach!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
//foreach文でキャラクター名を表示させたい
foreach ($sanrio_characters_assoc as $character) {
    echo $character["名前"] . "<br>";
}

//foreach文でキャラクターの年齢を表示させたい
foreach ($sanrio_characters_assoc as $character) {
    echo $character["誕生年"] . "<br>";
}
